package com.common.chart.bean.chart;

public class Chart {
	// 数据名称
	private String name;
	// 数据对应字段
	private String field;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getField() {
		return field;
	}
	public void setField(String field) {
		this.field = field;
	}
	
}
